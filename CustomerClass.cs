using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AceDrones
{
    // Class where the attributes of the Customer class are stored
    class CustomerClass
    {
        private string custID;
        private string fullName;
        private string city;
        private string country;
        // getters and setters for each of the attributes from the Customer class 
        public string gsCustID { get => custID; set => custID = value; }
        public string gsFullName { get => fullName; set => fullName = value; }
        public string gsCountry { get => country; set => country = value; }
        public string gsCity { get => city; set => city = value; }
    }
    







  }

